package com.datastat.result;

public enum ReturnCode {
    RC200(200, "success"),
    RC400(400, "query error");

    private final int code;
    private final String message;

    ReturnCode(int code, String message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }
}
